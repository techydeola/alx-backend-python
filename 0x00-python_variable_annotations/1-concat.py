#!/usr/bin/env python3

""" This is a module of a type-annotated function concat
    that takes a string str1 and a string str2 as arguments
    and returns a concatenated string
"""


def concat(str1: str, str2: str) -> str:
    """takes two args(str) and returns a concatenated string
        Return: str1 + str 2
    """
    return str1 + str2
